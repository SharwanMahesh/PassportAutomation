    private void registerUser() {
        try {
            String sql = "INSERT INTO users (name, age, gender, address, username, password) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, txtName.getText());
            pst.setInt(2, Integer.parseInt(txtAge.getText()));
            pst.setString(3, txtGender.getText());
            pst.setString(4, txtAddress.getText());
            pst.setString(5, txtUsername.getText());
            pst.setString(6, txtPassword.getText());
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Registration Successful");
            this.dispose();
            new LoginForm().setVisible(true);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this,"Error in registration");
        }
    }


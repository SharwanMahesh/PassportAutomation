    private void loadUserDetails() {
        try {
            String sql = "SELECT * FROM users WHERE username=?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, username);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                txtName.setText(rs.getString("name"));
                txtAge.setText(String.valueOf(rs.getInt("age")));
                txtGender.setText(rs.getString("gender"));
                txtAddress.setText(rs.getString("address"));
                txtUsername.setText(rs.getString("username"));
                txtPassword.setText(rs.getString("password"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void updateUserDetails() {
        String newName = txtName.getText();
        String newAge = txtAge.getText();
        String newGender = txtGender.getText();
        String newAddress = txtAddress.getText();
        String newUsername = txtUsername.getText();
        String newPassword = txtPassword.getText();

        if (newName.isEmpty() || newAge.isEmpty() || newGender.isEmpty() || newAddress.isEmpty() || newUsername.isEmpty() || newPassword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields are required!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            String sql = "UPDATE users SET name=?, age=?, gender=?, address=?, username=?, password=? WHERE username=?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, newName);
            pst.setInt(2, Integer.parseInt(newAge));
            pst.setString(3, newGender);
            pst.setString(4, newAddress);
            pst.setString(5, newUsername);
            pst.setString(6, newPassword);
            pst.setString(7, username);

            int rowsUpdated = pst.executeUpdate();
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this, "Details Updated Successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                new LoginForm().setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Update Failed!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error Updating Details!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    
